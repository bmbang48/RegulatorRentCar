    protected void datatable(){
        Object[] Baris={"No Induk","Nama Pelanggan","Alamat","No Telpon","Email"};
        tabmode = new DefaultTableModel(null,Baris);
        tabelPelanggan.setModel(tabmode);
        String sql="SELECT * FROM pelanggan WHERE nik LIKE '%"+tCari.getText()+"%' "
                + "OR nama LIKE '%"+tCari.getText()+"%' "
                + "OR alamat LIKE '%"+tCari.getText()+"%' "
                + "OR telp LIKE '%"+tCari.getText()+"%' "
                + "OR email LIKE '%"+tCari.getText()+"%'";
        try{
            java.sql.Statement stat=conn.createStatement();
            ResultSet hasil=stat.executeQuery(sql);
            while(hasil.next()){
                String a=hasil.getString("nik");
                String b=hasil.getString("nama");
                String c=hasil.getString("alamat");
                String d=hasil.getString("telp");
                String e=hasil.getString("email");
                String[] data={a,b,c,d,e};
                tabmode.addRow(data);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Tidak Bisa Menampilkan Tabel" + e);
        }
    }
    
