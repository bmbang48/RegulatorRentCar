    public void foto(String a){
        String sql = "SELECT foto_ktp FROM pelanggan Where nik = '"+a+"'";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String foto = hasil.getString("foto_ktp");
                ImageIcon icon = new ImageIcon(foto);
                Image img = icon.getImage().getScaledInstance(ktp.getWidth(), ktp.getHeight(), Image.SCALE_DEFAULT);
                ImageIcon ic = new ImageIcon(img);
                ktp.setIcon(ic);
            }
        }catch(Exception e) {
            
        }
    }
    
