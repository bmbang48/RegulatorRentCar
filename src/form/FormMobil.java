    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try{
            String sql = "UPDATE mobil SET merk=?,type=?,transmisi=?,tahun=?,jumlah_penumpang=?,konsumsi_bbm=?,kekuatan_mesin=?,kecepatan_maksimum=?,harga=?,status=? WHERE no_plat=?";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1,tMerk.getText());
            stat.setString(2,tType.getText());
            String transmisi="";
            if(rt1.isSelected()) transmisi="Automatic";
            else transmisi="Manual";
            stat.setString(3, transmisi);
            stat.setString(4,tTahun.getText());
            stat.setString(5,tPenumpang.getText());
            stat.setString(6,tBbm.getText());
            stat.setString(7,tMesin.getText());
            stat.setString(8,tKecepatan.getText());
            stat.setString(9,tHarga.getText());
            stat.setString(10,cStatus.getSelectedItem().toString());
            stat.setString(11,tNoPlat.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            kosong();
            tNoPlat.requestFocus();
            datatable();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Diubah "+e);
        }
    }//GEN-LAST:event_btnEditActionPerformed
