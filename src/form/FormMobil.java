    protected void datatable(){
        Object[] Baris={"No","Plat Nomor", "Merk", "Type", "Transmisi", "Status", "Tahun", "Harga", "Jumlah Penumpang", "Kekuatan Mesin", "Konsumsi Bbm", "Kecepatan Maksimum"};
        tabmode = new DefaultTableModel(null,Baris);
        tabelMobil.setModel(tabmode);
        String sql="SELECT * from mobil WHERE no_plat LIKE '%"+tCari.getText()+"%'"
                + "OR type LIKE '%"+tCari.getText()+"%'"
                + "OR merk LIKE '%"+tCari.getText()+"%'"
                + "OR transmisi LIKE '%"+tCari.getText()+"%'"
                + "OR status LIKE '%"+tCari.getText()+"%'"
                + "OR tahun LIKE '%"+tCari.getText()+"%'"
                + "OR harga LIKE '%"+tCari.getText()+"%'"
                + "OR jumlah_penumpang LIKE '%"+tCari.getText()+"%'"
                + "OR kekuatan_mesin LIKE '%"+tCari.getText()+"%'"
                + "OR konsumsi_bbm LIKE '%"+tCari.getText()+"%'"
                + "OR kecepatan_maksimum LIKE '%"+tCari.getText()+"%'";
        try{
            java.sql.Statement stat=conn.createStatement();
            ResultSet hasil=stat.executeQuery(sql);
            x=1;
            while(hasil.next()){  
                String a=hasil.getString("no_plat");
                String b=hasil.getString("merk");
                String c=hasil.getString("type");
                String d=hasil.getString("transmisi");
                String e=hasil.getString("status");
                String f=hasil.getString("tahun");
                String g=hasil.getString("harga");
                String h=hasil.getString("jumlah_penumpang");
                String i=hasil.getString("kekuatan_mesin");
                String j=hasil.getString("konsumsi_bbm");
                String k=hasil.getString("kecepatan_maksimum");
                String no=String.valueOf(x);
                String[] data={no,a,b,c,d,e,f,g,h,i,j,k};
                tabmode.addRow(data);
                x=x+1;
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Tidak Bisa Menampilkan Tabel "+e);
        }
    }
